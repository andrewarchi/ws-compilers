<h2>Whitespace Tools</h2>

<p>Aside from interpreters and compilers, there are some other tools available
for helping with whitespace development.</p>

<dl>
<dt><strong><a href="http://www.burghard.info/code/whitespace/index.html">
Interpreter, Assembler, and Interpreter in Whitespace</a></strong></dt>
<dd>
<p>
Several tools, written by Oliver Burghard, including what the world has been 
waiting for, a whitespace interpreter written in whitespace itself!
</p>
</dd>

<dt><strong><a href="whitespace-mode.el">Emacs mode</a></strong></dt>
<dd>
<p>
An emacs mode, written by <a href="mailyo:wence@gmx.li">Lawrence Mitchell</a>.
</p>
</dd>

<dt><strong><a href="embed.hs">embed</a></strong></dt>
<dd>
<p>
This is a simple tool, written in haskell, to embed a whitespace program
in a text file (replacing the spaces in the text file with whitespace syntax).
It's how I generated <a href="tutorial.html">the [fact|tut]orial</a> program.
An <a href="embed">executable</a> for Linux is also available.
</p>
</dd>

<dt><strong><a href="http://yagni.com/whitespace/index.html">Assembler</a></strong></dt>
<dd>
<p>
Written by Wayne Conrad, this is an assembler for those who don't fancy writing
whitespace directly. Cowards!
</p>
</dd>
</dl>


