<h2>Whitespace</h2>

<dl>
<dt><strong>What is Whitespace?</strong></dt>
<dd>
<p>
Most modern programming languages do not consider white space characters 
(spaces, tabs and newlines) syntax, ignoring them, as if they weren't there.
We consider this to be a gross injustice to these perfectly friendly members 
of the character set. Should they be ignored, just because they are invisible?
Whitespace is a language that seeks to redress the balance. Any non whitespace
characters are ignored; only spaces, tabs and newlines are considered syntax.
</p>
</dd>

<dt><strong>What are the advantages of Whitespace?</strong></dt>
<dd>
<p>
Some things which are difficult in other languages are made much easier in 
Whitespace. For example, literate programming is simply a matter of writing
your helpful comments in between program instructions. It's also easy to
encrypt your programs. Simply write a misleading comment!
</p>
<p>
Whitespace is a particularly useful language for spies. Imagine you have a top 
secret program that you don't want anyone to see. What do you do? Simply print
it out and delete the file, ready to type in at a later date. Nobody will know 
that your blank piece of paper is actually vital computer code!
</p>
</dd>

