<p>
The source should compile on any Unix machine with the Glasgow Haskell Compiler
version 5.02 or later. We're also informed that it compiles correctly under
OS X 10.2.4 with no problems.
</p>
<p>
However, this is an early beta version, so problems may still exist. Let us
know if you have success compiling on other architectures!</p>

