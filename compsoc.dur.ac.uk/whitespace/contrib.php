<h2>Contributions</h2>
<p>
Several whitespace users have sent in their contributions as examples of use 
of the language. Please get in touch if you have a program that you'd like to 
be added to this list.
</p>

<dl>
<dt><strong><a href="whitespace.ws.c">Multi language</a></strong></dt>
<dd>
<p>
Written by Jeremy James, this program is both valid whitespace and valid C, 
and does approximately the same no matter which language you choose to execute 
it in.
</p>

<dt><strong><a href="rot13.ws">ROT 13</a></strong></dt>
<dd>
<p>
A practical use for whitespace might be to embed this in a rot13ed text file.
Written by Chris Morris.
</p>
</dd>

<dt><strong><a href="quine.ws">Quine</a></strong></dt>
<dd>
<p>
Every language needs to have a quine, which is a program that outputs its
own source code. This is the first Whitespace quine, written by Benjamin 
Lerman.
</p>
</dd>

<dt><strong><a href="quine-2.ws">Another Quine</a></strong></dt>
<dd>
<p>
This is a second, shorter, quine, also written by Benjamin Lerman.
</p>
</dd>

<dt><strong><a href="quine-copy.ws">Yet Another Quine</a></strong></dt>
<dd>
<p>
A third quine, again by Benjamin Lerman, which includes a copyright notice,
and is probably the largest piece of whitespace code written so far...
</p>
<p>
It's possibly also motivation for me to improve the interpreter so that it
runs in a sensible time...
</p>
</dd>

</dl>

